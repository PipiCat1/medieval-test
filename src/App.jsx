function getRarityBadge(primary, secondary, specialKey, traits, inventory, npcMemory, flags) {
  let score = 0;
  if (specialKey) score += 2;
  if (inventory.length >= 4) score += 1;
  if (traits.weirdness >= 4) score += 1;
  if (traits.strategy >= 4 && traits.charm >= 4) score += 1;
  if ((npcMemory.cat || 0) >= 2 || (npcMemory.monk || 0) <= -2) score += 1;
  if (flags.includes('insultedMonk') || flags.includes('tookBell') || flags.includes('tookGlowingVial')) score += 1;

  if (score >= 5) {
    return {
      label: 'Очень редкий итог',
      text: 'Собралась настолько характерная комбинация привычек, репутации и находок, что город точно будет обсуждать тебя отдельно.',
    };
  }
  if (score >= 3) {
    return {
      label: 'Редкий итог',
      text: 'Такое сочетание пути, предметов и слухов выпадает нечасто.',
    };
  }
  return null;
}

