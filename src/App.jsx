function getAdaptiveEncounter(current, answerIndex, flags, inventory, npcMemory) {
  const answer = questions[current]?.answers?.[answerIndex];
  const base = answer?.encounter;
  if (!base) return null;

  const encounter = {
    ...base,
    choices: (base.choices || []).map((choice) => ({ ...choice })),
  };

  if (encounter.choices.length === 3) {
    const timidChoice = getTimidEncounterChoice(current, answerIndex, answer);
    if (timidChoice) encounter.choices.push(timidChoice);
  }

  if (inventory.includes('подозрительно прекрасный колокольчик') && current >= 4) {
    encounter.text += ' Где-то совсем некстати снова едва слышно звякает тот самый колокольчик.';
  }

  if (current === 5 && answerIndex === 0 && flags.includes('insultedMonk')) {
    encounter.title = 'Письмо приносит тот самый монах';
    encounter.text = 'Он узнаёт тебя сразу, смотрит с церковным неодобрением и всё равно протягивает письмо без печати так, будто это духовное испытание именно для тебя.';
    if (encounter.choices[1]) {
      encounter.choices[1].note = 'Монах явно не ожидал от тебя такого зрелого поведения и теперь немного озадачен.';
    }
  }

  if (current === 6 && answerIndex === 1 && inventory.includes('светящийся флакон')) {
    encounter.title = 'В саду тебя уже поджидает алхимик';
    encounter.text = 'Он делает вид, что пришёл проверить розмарин, но на самом деле смотрит на твой светящийся флакон так, будто вы уже связаны одной очень сомнительной научной линией.';
  }

  if (current === 7 && answerIndex === 0 && (npcMemory.cat || 0) >= 2) {
    encounter.text += ' На подоконнике сидит знакомый кот и смотрит так, будто будет проверять качество твоих выводов.';
  }

  if (current === 8 && answerIndex === 2 && (npcMemory.actor || 0) >= 1) {
    encounter.title = 'Актёр снова находит тебя первым';
    encounter.text = 'Бродячий актёр утверждает, что именно ты подходишь на роль человека, который умеет носить тайну, драму и капюшон так, будто это официальная должность.';
  }

  if (current === 9 && answerIndex === 0 && flags.includes('betrayedSecret')) {
    encounter.title = 'Последнюю страницу тебе вручают с заметной осторожностью';
    encounter.text = 'Хронист явно слышал кое-какие городские версии и теперь смотрит на тебя как на человека, которому можно доверить перо, но не всегда чужую исповедь.';
  }

  if (current === 9 && answerIndex === 1 && (npcMemory.knight || 0) >= 2) {
    encounter.text += ' Где-то неподалёку тот самый рыцарь уже готов считать твой выбор личным доказательством того, что мир ещё можно спасти.';
  }

  return encounter;
}

function getQuietMarks(flags, inventory, npcMemory, traits) {
  const marks = [];

  if ((npcMemory.cat || 0) >= 2) marks.push('собрал уважение кота');
  if (traits.reserve >= 4) marks.push('умеет молчать выразительнее многих речей');
  if (traits.awkwardness >= 2) marks.push('превратил неловкость в отдельный почерк');
  if ((npcMemory.monk || 0) <= -2) marks.push('оставил после себя церковную напряжённость');
  if (inventory.includes('маршрут непонятно, но красиво')) marks.push('однажды выбрал непонятно, но красиво — и не свернул');
  if (inventory.includes('чужой компромат')) marks.push('слишком спокойно носит при себе чужие проблемы');
  if (flags.includes('betrayedSecret')) marks.push('однажды превратил доверие в городской транспорт');
  if (inventory.includes('подозрительно прекрасный колокольчик')) marks.push('доказал, что колокольчик может быть чертой характера');
  if (traits.practicality >= 4) marks.push('умеет превращать хаос в рабочую конструкцию');
  if (traits.weirdness >= 4) marks.push('официально замечен как красивая странность эпохи');

  return marks.slice(0, 5);
}

