function getWorldEcho(flags, inventory, npcMemory) {
  if (flags.includes('insultedMonk')) {
    return 'После истории с монахом некоторые двери как будто открываются с лёгким осуждением.';
  }
  if (inventory.includes('подозрительно прекрасный колокольчик')) {
    return 'Колокольчик всё ещё при тебе. Мир продолжает делать вид, что это нормально.';
  }
  if (inventory.includes('чужой компромат')) {
    return 'Ты всё ещё знаешь слишком много, и это уже немного отражается в осанке.';
  }
  if ((npcMemory.cat || 0) >= 2) {
    return 'Иногда кажется, что где-то рядом снова наблюдает тот самый кот. Это довольно сильное одобрение.';
  }
  if (inventory.length >= 3) {
    return 'За тобой уже тянется небольшой шлейф предметов, слухов и последствий.';
  }
  return '';
}

function getSceneWhisper(current, phase, flags, inventory, npcMemory) {
  const has = (flag) => flags.includes(flag);

  if (current < 2) return '';
  if (has('insultedMonk') && current >= 5) {
    return 'По городу уже ходит версия, что монастырь называет тебя «талантливой проблемой».';
  }
  if ((npcMemory.cat || 0) >= 2 && current >= 4) {
    return 'Где-то неподалёку снова мелькает знакомый кот. Вид у него такой, будто он давно включил тебя в личный список наблюдений.';
  }
  if (inventory.includes('подозрительно прекрасный колокольчик') && current >= 4) {
    return 'Колокольчик время от времени едва слышно звенит, как будто заранее оценивает твои будущие решения.';
  }
  if (inventory.includes('светящийся флакон') && current >= 7) {
    return 'Флакон в сумке снова мерцает. Никто не комментирует это вслух, и именно это почему-то тревожит сильнее всего.';
  }
  if ((npcMemory.chamberlain || 0) >= 1 && current >= 6) {
    return 'При дворе тебя уже запомнили, и это то ли хорошо, то ли первый симптом осложнений.';
  }
  if ((npcMemory.blacksmith || 0) >= 2 && current >= 5) {
    return 'Где-то в кузнице о тебе отзываются уважительно. Это почти отдельная форма героического признания.';
  }
  if (has('watchedOmens') && current >= 6) {
    return 'Иногда ветер звучит так, будто всё ещё пытается с тобой договорить.';
  }
  return '';
}

