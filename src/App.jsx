function scoreAnswers(answerIndexes) {
  const totals = {
    scribe: 0,
    healer: 0,
    minstrel: 0,
    armorer: 0,
    falconer: 0,
    steward: 0,
    guard: 0,
    cartographer: 0,
  };

  answerIndexes.forEach((answerIndex, qIndex) => {
    const answer = questions[qIndex]?.answers?.[answerIndex];
    if (!answer) return;
    Object.entries(answer.score || {}).forEach(([key, value]) => {
      totals[key] += value;
    });
  });

  const sorted = Object.entries(totals).sort((a, b) => b[1] - a[1]);
  const primary = sorted[0]?.[0] || 'scribe';
  const secondary = sorted[1]?.[0] || 'healer';

  return { totals, primary, secondary };
}

